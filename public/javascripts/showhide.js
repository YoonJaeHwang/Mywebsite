function Manchester_city() {
  var x = document.getElementById("team_explain"); // id가 team_explain을 x로 변수 지정

  if (x.style.display === "block") { 
    x.style.display = "none"; // 사라짐
  } else {
    x.style.display = "block"; // 보여줌
  }
};

